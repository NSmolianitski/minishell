#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;
	t_list	*temp2;

	temp = *lst;
	while (temp)
	{
		temp2 = temp->next;
		(*del)(temp->content);
		free(temp);
		temp = temp2;
	}
	*lst = 0;
}
